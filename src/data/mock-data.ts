export const generateMetricsHistory = (hours: number = 24): ServerMetric[] => {
  const metrics: ServerMetric[] = [];
  const now = new Date();
  for (let i = hours; i >= 0; i--) {
    const timestamp = new Date(now.getTime() - i * 3600000);
    metrics.push({
      timestamp: timestamp.toISOString(),
      cpu: 15 + Math.random() * 40 + (i % 6 === 0 ? 20 : 0),
      ram: 40 + Math.random() * 25,
      disk: 52 + Math.random() * 3,
      networkIn: Math.random() * 50,
      networkOut: Math.random() * 30,
    });
  }
  return metrics;
};

